    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int linha = tabelaCreas.getSelectedRow();
        Object[] options = {"Sim", "Não"};
        if (linha == -1) {
            JOptionPane.showMessageDialog(rootPane, "Selecione uma linha!");
        } else if (JOptionPane.showOptionDialog(null, "Deseja mesmo excluir esse Cadastro?", "Excluir Cadastro",
                JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, null, options, options[0]) == JOptionPane.YES_OPTION) {
            creas = creasDAO.pesquisarCreasPorID((int) tabelaCreas.getValueAt(linha, 0));
            creasDAO.deletar(creas);
            JOptionPane.showMessageDialog(null, "Cadastro excluído com sucesso");
            atualizarTabela();
        }
        
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        int linha = tabelaCreas.getSelectedRow();
        if (linha == -1) {
            JOptionPane.showMessageDialog(rootPane, "Selecione uma linha!");
        } else {
            creas = creasDAO.pesquisarCreasPorID((int) tabelaCreas.getValueAt(linha, 0));
            FichaNotificacaoCreas fichaNotificacaoCreas = new FichaNotificacaoCreas(this.usuario);
            fichaNotificacaoCreas.setVisible(true);
            fichaNotificacaoCreas.creas = creas;
            fichaNotificacaoCreas.atualizarConsulta();
            fichaNotificacaoCreas.setVisible(true);
            this.dispose();
        }
        
    }//GEN-LAST:event_btnEditarActionPerformed

