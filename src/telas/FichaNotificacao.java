    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed

        if ((preencherPessoa() && preencherDadosResidencia() && preencherDados() && preencherOcorrencia())) {

            preencherDados();
            preencherPessoa();
            preencherDadosResidencia();
            preencherOcorrencia();
            
            if(vitima.getIdVitima()!= 0){
                vitimaDAO.atualizar(vitima);
                JOptionPane.showMessageDialog(null, "Ficha atualizada com sucesso!");
            } else {
                vitimaDAO.salvar(vitima);
                JOptionPane.showMessageDialog(null, "Ficha cadastrada com sucesso!");
            }
            
            limparCampos();
        } else {
            JOptionPane.showMessageDialog(null, "Campos obrigatórios não preenchidos!");
        }

    }//GEN-LAST:event_btnSalvarActionPerformed

