    private boolean preencherOcorrencia() {

        if (!((txtLogradouroDadosOcorrencia.getText().isEmpty()) || (txtMunicipioOcorrenciaDadosOcorrencia.getText().isEmpty()) || (txtNumeroDadosOcorrencia.getText().isEmpty()))) {
            vitima.setMunicipioOcorrencia(txtMunicipioOcorrenciaDadosOcorrencia.getText());
            vitima.setLogradouroOcorrencia(txtLogradouroDadosOcorrencia.getText());
            vitima.setNumeroOcorrencia(txtNumeroDadosOcorrencia.getText());
            vitima.setLocalOcorrencia(jcLocalDadosOcorrencia.getSelectedItem().toString());
            vitima.setOutroLocalOcorrencia(txtOutroLocalDadosOcorrencia.getText());
            vitima.setUfOcorrencia(txtUfDadosOcorrencia.getText());
            vitima.setBairroOcorrencia(txtBairroDadosOcorrencia.getText());
            vitima.setComplementoOcorrencia(txtComplementoDadosOcorrencia.getText());
            vitima.setZonaOcorrencia(jcZonaDadosOcorrencia.getSelectedItem().toString());
            vitima.setOcorreuQuantasVezes(jcOcorreuOutrasDadosOcorrencia.getSelectedItem().toString());
            vitima.setLesaoAutoprovocada(jcLesaoAutoDadosOcorrencia.getSelectedItem().toString());
            vitima.setTipoViolencia(jcTipoViolenciaDadosOcorrencia.getSelectedItem().toString());
            vitima.setTipoViolenciaOutro(txtOutroViolenciaDadosOcorrencia.getText());
            return true;
        }

        return false;
    }


